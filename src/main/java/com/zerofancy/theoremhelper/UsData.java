package com.zerofancy.theoremhelper;

public class UsData {
	Long id;
	String abs;
	String content;
	Long sub;
	String sub_str;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getAbs() {
		return abs;
	}
	public void setAbs(String abs) {
		this.abs = abs;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Long getSub() {
		return sub;
	}
	public void setSub(Long sub) {
		this.sub = sub;
	}
	public String getSub_str() {
		return sub_str;
	}
	public void setSub_str(String sub_str) {
		this.sub_str = sub_str;
	}
}
